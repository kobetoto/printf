#include "ft_printf.h"

size_t ft_putnbr(int n)
{
    char    c;

    if (n < 0)
    {
        ft_putchar('-');
        if(n == -2147483648)
        {
            write(1,"2147483648",10);
            return (11);
        }
        n = -n;
    }
    if (n >= 0)
        ft_putnbr(n / 10);
    c = (n % 10) + '0';
    return (ft_putchar(c));
}
